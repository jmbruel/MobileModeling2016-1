        <!-- iOS -->
        <h3 id="iOS">iOS:</h3>
        <p id="blocTextiOS">
            iOS, anciennement iPhone OS, est le système d'exploitation mobile développé par Apple pour plusieurs de ses appareils.</br></br> 
            
            Il est dérivé de OS X dont il partage les fondations (le noyau hybride XNU basé sur le micro-noyau Mach, les services Unix et Cocoa, etc.). iOS comporte quatre couches d'abstraction, similaires à celles de Mac OS X : une couche «Core OS», une couche «Core Services», une couche «Media» et une couche «Cocoa». Le système d'exploitation occupe au maximum 3 Go de la capacité mémoire totale de l'appareil, selon l'appareil. (wiki)
        </p>
