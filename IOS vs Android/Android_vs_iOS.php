        <!-- Android -->
        <h3 id="android">Android:</h3>
        <p id="blocTextAndroid">
            Android est un système d'exploitation mobile 3,4 basé sur le noyau Linux et développé actuellement par Google.</br></br>

            Lancé en juin 2007 à la suite du rachat par Google en 2005 de la startup du même nom, le système avait d'abord été conçu pour les smartphones et tablettes tactiles, puis s'est diversifié dans les objets connectés et ordinateurs comme les télévisions (Android TV), les voitures (Android Auto), les ordinateurs (Android-x86) et les smartwatchs (Android Wear).</br></br>

            En 2015, Android est, face à iOS d'Apple, le système d'exploitation le plus utilisé dans le monde avec plus de 80 % de parts de marché dans les smartphones. (Wiki)
        </p>
        </br>
